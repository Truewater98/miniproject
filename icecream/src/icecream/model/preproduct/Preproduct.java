package icecream.model.preproduct;

public class Preproduct {
	private int prouductCode;
	private String productName;
	private int amount;
	private String eventYn;
	private int eventCode;
	
	public int getProuductCode() {
		return prouductCode;
	}
	public void setProuductCode(int prouductCode) {
		this.prouductCode = prouductCode;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public String getEventYn() {
		return eventYn;
	}
	public void setEventYn(String eventYn) {
		this.eventYn = eventYn;
	}
	public int getEventCode() {
		return eventCode;
	}
	public void setEventCode(int eventCode) {
		this.eventCode = eventCode;
	}
	
	
}
